package entities;

public class Habitaciones {
	
	private int id_hab;
	private int id_tipo_hab;
	private int id_estado_hab;
	private int nro_hab;
	private String desc_hab;
	private String habitacionescol;
	
	public int getId_hab() {
		return id_hab;
	}
	public void setId_hab(int id_hab) {
		this.id_hab = id_hab;
	}
	public int getId_tipo_hab() {
		return id_tipo_hab;
	}
	public void setId_tipo_hab(int id_tipo_hab) {
		this.id_tipo_hab = id_tipo_hab;
	}
	public int getId_estado_hab() {
		return id_estado_hab;
	}
	public void setId_estado_hab(int id_estado_hab) {
		this.id_estado_hab = id_estado_hab;
	}
	public int getNro_hab() {
		return nro_hab;
	}
	public void setNro_hab(int nro_hab) {
		this.nro_hab = nro_hab;
	}
	public String getDesc_hab() {
		return desc_hab;
	}
	public void setDesc_hab(String desc_hab) {
		this.desc_hab = desc_hab;
	}
	public String getHabitacionescol() {
		return habitacionescol;
	}
	public void setHabitacionescol(String habitacionescol) {
		this.habitacionescol = habitacionescol;
	}
	

}
